-- spawn timer must be a specific range for E2 and E3 to drop gold as soon as they leave the O
while not c.IsDone() do
    c.GrabLadderLeft()
    c.ClimbUntil(1)
    c.RightUntil(6)
    c.UntilGoldRight()
    c.UntilGoldRight()
    c.LeftUntil(8)
    c.PushDown()
    c.FinishFalling()
    c.FallLeft()
    c.FallLeft()
    c.FallLeft()
    c.RightFor(2)
    c.GrabLadderRight('Down')
    c.ClimbUntil(13)

    c.WaitFor(15)
    c.Assert(c.FrameSearch(function()
        local result = c.ClimbUntil(12)
        if not result then return false end
        result = c.UntilGoldLeft()
        if not result then return false end
        result = c.UntilGoldLeft()
        if not result then return false end
        return c.RightFor(1)
    end))

    c.BestOf({
        function() return c.RightUntil(16) end,
        function()
            c.GrabLadderRight('Down')
            c.ClimbUntil(13)
            return c.RightUntil(16)
        end,
    })

    c.GrabLadderRight()
    c.ClimbUntil(12)
    c.RightFor(5)
    c.GrabLadderRight()
    c.ClimbUntil(1)
    c.LeftUntil(20)

    c.BestOf({
        DigRoute,
        V5Route,
    })

    c.Done()

    --c.Marker('lv 34 end')
end

c.Finish()
