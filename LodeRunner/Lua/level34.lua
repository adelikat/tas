    c.ClimbUntil(4)
    c.FinishFalling()
    c.FallLeft()
    c.GrabLadderLeft()

    c.ClimbUntil(9)
    c.LeftFor(1)
    c.GrabLadderLeft()
    c.ClimbUntil(8)
    c.GrabLadderLeft()
    c.ClimbUntil(7)
    c.GrabLadderLeft()
    c.ClimbUntil(5)
    c.GrabLadderRight('Down')
    c.ClimbUntil(6)
    c.FinishFalling()
    c.GrabLadderLeft()
    c.ClimbUntil(5)
    c.GrabLadderRight()
    c.ClimbUntil(4)
    c.GrabLadderRight('Down')
    c.ClimbUntil(5)
    c.FinishFalling()

    c.GrabLadderLeft()
    c.ClimbUntil(7)
    c.GrabLadderLeft()
    c.ClimbUntil(5)
    c.GrabLadderRight()
    c.ClimbUntil(4)
    c.GrabLadderRight()
    c.ClimbUntil(3)
    c.GrabLadderRight('Down')
    c.ClimbUntil(5)
    c.FinishFalling()
