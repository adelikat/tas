-- Starts on the frame immediately after pressing select to do the select skip
dofile('lode-runner-core.lua')

c.Start()

if c.CurrentLevel() ~= 34 then
    error('must be run in level 34')
end

if c.GameMode() ~= 1 then
    error('must be run in level mode')
end

local function DigRoute()
    c.UntilGoldLeft()
    c.UntilDig('Left', 'A')
    c.FallRight()
    c.UntilGoldRight()
    c.UntilDig('Left', 'A')
    c.FallRight()
    c.UntilGoldRight()
    c.UntilDig('Left', 'A')
    c.FallRight()
    c.UntilDig('Right', 'B')
    c.FallLeft()
    c.GrabLadderLeft()
    c.ClimbUntil(4)
    c.GrabLadderLeft()
    c.ClimbUntil(3)
    c.LeftFor(1)
    c.ClimbUntil(4)
    c.FinishFalling()
    c.FallLeft()
    c.GrabLadderLeft()
    c.ClimbUntil(9)
    c.LeftFor(1)
    c.GrabLadderLeft()
    c.ClimbUntil(8)
    c.GrabLadderLeft()
    c.ClimbUntil(7)
    c.GrabLadderLeft()
    c.ClimbUntil(5)
    c.GrabLadderRight('Down')
    c.ClimbUntil(6)
    c.FinishFalling()
    c.GrabLadderLeft()
    c.ClimbUntil(5)
    c.GrabLadderRight()
    c.ClimbUntil(4)
    c.GrabLadderRight('Down')
    c.ClimbUntil(5)
    c.FinishFalling()
    c.GrabLadderLeft()
    c.ClimbUntil(7)
    c.GrabLadderLeft()
    c.ClimbUntil(5)

    --c.GrabLadderRight()
    c.PushRight() --Ugh
    c.PushUpAndRight()

    c.ClimbUntil(4)
    c.GrabLadderRight()
    c.ClimbUntil(3)
    c.RightFor(1)
    c.ClimbUntil(4)
    c.FinishFalling()
    c.FallRight()
    c.GrabLadderRight()
    c.ClimbUntil(9)
    c.RightFor(1)
    c.GrabLadderRight()
    c.ClimbUntil(8)
    c.GrabLadderRight()
    c.ClimbUntil(7)
    c.GrabLadderRight()
    c.ClimbUntil(5)
    c.GrabLadderLeft()
    c.ClimbUntil(4)
    c.GrabLadderLeft('Down')
    c.ClimbUntil(5)
    c.FinishFalling()
    c.GrabLadderRight()
    c.ClimbUntil(7)
    c.GrabLadderRight()
    c.ClimbUntil(5)
    c.GrabLadderLeft('Down')
    c.ClimbUntil(6)
    c.FinishFalling()
    c.ClimbUntil(8)
    c.GrabLadderRight('Down')
    c.ClimbUntil(9)
    c.FinishFalling()
    c.RightFor(5)
    c.GrabLadderRight()
    c.ClimbUntilLevelEnd()

    return true
end

local function V5Route()
